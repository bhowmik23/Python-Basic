# operators in python

# Arithmetic operators
print("5+6 is ", 5+6)
print("5-6 is ", 5-6)
print("5*6 is ", 5*6)
print("5/6 is ", 5/6)
print("5%6 is ", 5%6)
print("5**3 is ", 5**3)
print("15//6 is ", 15//6)

# Assignment operators
x=5
print(x)
x+=7
print(x)

# Comparison operators
i=5
print(i==5)
print(i!=5)
print(i>=5)
print(i<=5)
print(i and 5)

# Logical operators
a=True
b=False

print(a and b)
print(a and a)
print(a or b)

# Identity operators
a=True
b=False
print(a is b)
print(a is not b)
print(6 is not 7)

# Membership operators
list =[3, 4, 6, 7, 2, 8]

print(32 in list)
print(2 in list)

# Bitwise operators
# 0 - 00
# 1 - 01
# 2 - 10
# 3 - 11
print(0 & 1)
print(0 | 1)
print(0 | 3)
